def get_sample_chars() -> list:
    seen = set()
    unique_chars = []
    for c in SAMPLE_CHARS:
        key = (c["char"], c["shengmu"], c["yunmu"], c["shengdiao"])
        if key not in seen:
            seen.add(key)
            char_data = c.copy()
            char_data["shengmu_order"] = SHENGMU_ORDER.get(char_data["shengmu"])
            char_data["yunmu_order"] = YUNMU_ORDER.get(char_data["yunmu"])
            unique_chars.append(GuangyunCharCreate(**char_data))
    return unique_chars


